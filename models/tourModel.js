// QUERY MIDDLEWARE
// tourSchema.pre('find', function (next) {
tourSchema.pre(/^find/, function (next) {
  //'find' makes a query middleware. this will point to current query and not the current document
  this.find({ secretTour: { $ne: true } });
  this.start = Date.now();
  next();
});

tourSchema.post(/^find/, function (docs, next) {
  //   console.log(docs);
  console.log(`query took: ${Date.now() - this.start} milliseconds!`);
  next();
});

//AGGREGATION MIDDLEWARE
tourSchema.pre('aggregate', function (next) {
  this.pipeline().unshift({ $match: { secretTour: { $ne: true } } });
  console.log(this.pipeline()); //this points to current aggragtion object
  next();
});
const Tour = mongoose.model('Tour', tourSchema); //creating a mongoose model
module.exports = Tour;
